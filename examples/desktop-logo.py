class DemoApp(Application):

    def initMenuBar(self, bounds: Rect) -> MenuBar:
        bounds.bottomRight.y = bounds.topLeft.y + 1
        return MenuBar(bounds, Menu(MenuItem("~A~bout", cmAbout, kbAltA, hcNoContext, 0)))

    def initDesktop(self, bounds: Rect) -> Desktop:
        bounds.topLeft.y += 1
        bounds.bottomRight.y -= 1
        return NewDesktop(bounds)

    def handleEvent(self, event: Event):
        super().handleEvent(event)
        if event.what == evCommand:
            if event.message.command == cmAbout:
                self.AboutDialog()
                self.clearEvent(event)

    def AboutDialog(self):
        d = Dialog(Rect(0, 0, 35, 12), 'About')
        d.options |= ofCentered
        d.insert(
            StaticText(Rect(1, 2, 34, 7),
                       """\x03Turbo Vision Example\n\n\x03Modifying the desktop\n\n\x03Borland Technical Support"""))
        d.insert(Button(Rect(3, 9, 32, 11), "~O~k", cmOK, bfDefault))
        self.desktop.execView(d)
        self.destroy(d)


class NewDesktop(Desktop):

    def initBackground(self, bounds: Rect):
        return NewBackground(bounds, PATTERN)


class NewBackground(Background):
    cpBackground = '\x26'

    def draw(self):
        w = len(lines[0])
        h = len(lines)
        b = DrawBuffer()
        leftX = (self.size.x - w) // 2
        rightX = leftX + w
        topY = (self.size.y - h) // 2
        bottomY = topY + h

        for i in range(self.size.y):
            for j in range(self.size.x):

                if leftX <= j < rightX and topY <= i < bottomY:
                    b.moveChar(j, lines[i - topY][j - leftX], self.getColor(0x01), 1)
                else:
                    b.moveChar(j, self._pattern, self.getColor(0x01), 1)
            self.writeLine(0, i, self.size.x, 1, b)


def setupLogging():
    vLogger = logging.getLogger('vindauga')
    vLogger.propagate = False
    lFormat = "%(name)-25s | %(message)s"
    vLogger.setLevel(logging.DEBUG)
    handler = logging.StreamHandler(open('vindauga.log', 'wt'))
    handler.setFormatter(logging.Formatter(lFormat))
    vLogger.addHandler(handler)


if __name__ == '__main__':
    setupLogging()
    myApp = DemoApp()
    myApp.run()
